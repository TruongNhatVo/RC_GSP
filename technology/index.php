                        <h4 class="title-sub-large">分子間相互作用測定装置 Octetによる<br>高親和性抗体クローン選別</h4>

                        <p>基本的なスクリーニング法は、抗原と抗体ライブラリーを混合し、抗原に結合しない抗体提示ファージを洗浄・除去し抗原に結合したファージを回収します。この一連の操作をパンニングと呼びます。
                            <br>
                            <br> 抗原の固相化法・洗浄法に関する当社独自のノウハウを駆使し、高親和性抗体のスクリーニング効率を向上させています。また高親和性抗体を提示するファージはトリプシン消化により抗体部分を切断して確実に回収します。一般的な酸やアルカリ液では有用な高親和性抗体を提示するファージの回収率が低いので注意が必要です。回収したファージは大腸菌に再感染させ、ヘルパーファージの重感染によってファージタンパクを産生させ、新たな抗体提示ファージを調製し、次のパンニングに供します。
                            <br>
                            <br> 通常３回のパンニングの後、回収したファージを感染させた大腸菌をクローニングし、個々のクローンから抗体提示ファージを産生させ、抗原結合能を評価し選別します。
                            <br> 得られた抗体遺伝子を利用し、IgG型抗体やscFv-F融合抗体への改変も行っています。
                        </p>
